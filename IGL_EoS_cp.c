double interpc( coord_t* f, double x, int n )
{
   int i;
    for( i = 1; i < n-1; i++ )
    {
        if ( f[i].x <= x && f[i+1].x >= x )
        {
	  double diffx = x - f[i].x;
	  double diffn = f[i+1].x - f[i].x;

	  return f[i].y + (f[i+1].y - f[i].y)*diffx/diffn;
        }
    }
    if(x <= f[1].x)
      {
	return f[1].y;
      } 
    else if (x >= f[5000].x)
      {
      return f[5000].y;
      }
 
    return f[1].y;
}

